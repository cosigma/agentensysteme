package de.hsb.ants.msg;

import de.hsb.ants.Color;

public class AntMessage {

	private String type;
	private Color color;
	
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public Color getColor() {
		return color;
	}
	public void setColor(Color color) {
		this.color = color;
	}

}
